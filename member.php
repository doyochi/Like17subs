        The group consists of 13 members divided into three sub-units, each with a different area of specialization: 
        'Hip-Hop Unit', 'Vocal Unit', and 'Performance Unit'. Seventeen has released three studio albums and ten extended plays.
        Seventeen is considered a "self-producing" idol group, with the members being actively involved in the 
        songwriting, choreographing, and other aspects of their performance.
